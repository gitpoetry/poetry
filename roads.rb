i find our trust in roads interesting.
driving through the mountains of colorado
at night
on roads i have never seen
the crest of the hill looks like a cliff
59 miles per hour	
but i press the pedal

driving by green mountain reservoir
the moon reflects off the frozen water
illuminating mount powell 
i can't help but look
i don't care about the ice-covered turn ahead
this is where i fly to in my dreams
when i try to forget the thrill
	of being in your arms
	again

eisnenhower tunnel:
too long to hold your breath
let's try
